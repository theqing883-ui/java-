//位运算符(都要转换为补码后再运算)
// & | ~ ^ >> << >>>
public class BitOperator{
	public static void main(String[] args) {
		//2的补码：00000000 00000000 00000000 00000010
		//3的补码：00000000 00000000 00000000 00000011
		System.out.println(2&3);// 00000000 00000000 00000000 00000010

		//-2的原码：10000000 00000000 00000000 00000010
		//-2的补码：11111111 11111111 11111111 11111110
		System.out.println(~-2);// 00000000 00000000 00000000 00000001

		//2的补码：00000000 00000000 00000000 00000010
		System.out.println(~2);//
		//运算后的补码：11111111 11111111 11111111 11111101->
		//运算后的反码：11111111 11111111 11111111 11111100->
		//运算后的原码：10000000 00000000 00000000 00000011

		//2的补码：00000000 00000000 00000000 00000010
		//3的补码：00000000 00000000 00000000 00000011
		System.out.println(2|3);
		//运算后的补码：00000000 00000000 00000000 00000011->
		//运算后的原码：00000000 00000000 00000000 00000011

		//2的补码：00000000 00000000 00000000 00000010
		//3的补码：00000000 00000000 00000000 00000011
		System.out.println(2^3);
		//运算后的补码：00000000 00000000 00000000 00000001->
		//运算后的原码：00000000 00000000 00000000 00000001


		//>>算术右移：低位溢出，符号位不变，并用符号位补缺失的高位(除以2)
		//<<算术左移：符号位不变，低位补零（乘以2）
		//>>>无符号右移：低位溢出，高位补零
		int a = 1 >> 2;
		System.out.println(a);

		//00000000 00000000 00000000 00000001->
		//00000000 00000000 00000000 00000000
		int b = 1 << 2;
		System.out.println(b);
		//00000000 00000000 00000000 00000001->
		//00000000 00000000 00000000 00000100

		int c = -1 >> 2;
		System.out.println(c);//-1
		//-1原码10000000 00000000 00000000 00000001->
		//-1补码11111111 11111111 11111111 11111111->
		//算术右移2位后的补码：11111111 11111111 11111111 11111111->
		//反码：11111111 11111111 11111111 11111110->
		//原码：10000000 00000000 00000000 00000001

		int d = -1 << 2;
		System.out.println(d);//-4
		//-1原码10000000 00000000 00000000 00000001->
		//-1补码11111111 11111111 11111111 11111111->
		//算术左移2位后的补码：11111111 11111111 11111111 11111100->
		//反码：11111111 11111111 11111111 11111011->
		//原码：10000000 00000000 00000000 00000100

		int e = 3 >>> 2;
		System.out.println(e);
		//3原码00000000 00000000 00000000 00000011->
		//3补码00000000 00000000 00000000 00000011->
		//无符号右移2位后的补码：00000000 00000000 00000000 00000000->
		//反码：00000000 00000000 00000000 00000000->
		//原码：00000000 00000000 00000000 00000000

		//计算机中有小数参与的计算结果都是近似值，通常不会用来比较大小
		System.out.println(-10.5%3);



	}	


}